metros = float(input('Digite uma distância em metros: '))
km = metros / 1000
hm = metros / 100
dam = metros / 10
dm = metros * 10
cm = metros * 100
mm = metros * 1000

print(f'A distância de {metros} metros corresponde a:')
print(f'{km} em km')
print(f'{hm} em hm')
print(f'{dam} em dam')
print(f'{dm} em dm')
print(f'{cm} em cm')
print(f'{mm} em mm')
